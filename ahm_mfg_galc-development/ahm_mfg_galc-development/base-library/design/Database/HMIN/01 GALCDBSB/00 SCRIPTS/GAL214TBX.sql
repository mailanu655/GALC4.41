  ALLOW REVERSE SCANS
  COMPRESS NO;
  
CREATE INDEX GALADM.GAL214_I2
  ON GALADM.GAL214TBX
    ( LINE_ID ASC, PROCESS_POINT_ID ASC )
  ALLOW REVERSE SCANS
  COMPRESS NO;
  
