DROP TRIGGER GALADM.NOTIFICATION_TR2;
DROP TRIGGER GALADM.NOTIFICATION_TR1;
DROP TABLE GALADM.NOTIFICATION_TBX;

CREATE TABLE GALADM.NOTIFICATION_TBX (
  NOTIFICATION_CLASS VARCHAR(256) NOT NULL,
  DESCRIPTION VARCHAR(256),
  CLIENT_ONLY SMALLINT DEFAULT 0,
  CREATE_TIMESTAMP TIMESTAMP,
  UPDATE_TIMESTAMP TIMESTAMP,
  CONSTRAINT NOTIFICATION_PK PRIMARY KEY
    (NOTIFICATION_CLASS)
)
  IN GALTBSDAT1
  INDEX IN GALTBSIDX1;

ALTER TABLE GALADM.NOTIFICATION_TBX
  DATA CAPTURE CHANGES INCLUDE LONGVAR COLUMNS
  PCTFREE 0
  LOCKSIZE ROW
  APPEND OFF
  NOT VOLATILE;

COMMENT ON TABLE GALADM.NOTIFICATION_TBX IS
   'RMT Notification class definition for asynchronous communication';

CREATE TRIGGER GALADM.NOTIFICATION_TR1
   NO CASCADE BEFORE INSERT
   ON GALADM.NOTIFICATION_TBX
   REFERENCING NEW AS A
   FOR EACH ROW
SET CREATE_TIMESTAMP = CURRENT_TIMESTAMP;

CREATE TRIGGER GALADM.NOTIFICATION_TR2
   NO CASCADE BEFORE UPDATE
   ON GALADM.NOTIFICATION_TBX
   REFERENCING NEW AS A
   FOR EACH ROW
SET UPDATE_TIMESTAMP = CURRENT_TIMESTAMP;