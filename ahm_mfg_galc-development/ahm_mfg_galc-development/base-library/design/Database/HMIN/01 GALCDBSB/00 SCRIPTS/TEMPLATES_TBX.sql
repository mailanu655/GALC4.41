DROP TRIGGER GALADM.TEMPLATES_TR2;
DROP TRIGGER GALADM.TEMPLATES_TR1;
DROP TABLE GALADM.TEMPLATES_TBX;

CREATE TABLE GALADM.TEMPLATES_TBX (
  TEMPLATE_NAME VARCHAR(40) NOT NULL,
  TEMPLATE_DESCRIPTION VARCHAR(128),
  TEMPLATE_TYPE VARCHAR(32),
  TEMPLATE_DATA BLOB(10M)  INLINE LENGTH 196 LOGGED COMPACT,
  CREATE_TIMESTAMP TIMESTAMP,
  UPDATE_TIMESTAMP TIMESTAMP,
  FORM_ID VARCHAR(32),
  REVISION_ID INTEGER,
  CONSTRAINT TEMPLATES_PK PRIMARY KEY
    (TEMPLATE_NAME)
)
  IN GALTBS32K1
  INDEX IN GALTBSIDX4;

ALTER TABLE GALADM.TEMPLATES_TBX
  DATA CAPTURE CHANGES INCLUDE LONGVAR COLUMNS
  PCTFREE 0
  LOCKSIZE ROW
  APPEND OFF
  NOT VOLATILE;

COMMENT ON TABLE GALADM.TEMPLATES_TBX IS
   'RMT Template for LOT CONTROL to define Printing Templates';

CREATE TRIGGER GALADM.TEMPLATES_TR1
   NO CASCADE BEFORE INSERT
   ON GALADM.TEMPLATES_TBX
   REFERENCING NEW AS A
   FOR EACH ROW
SET CREATE_TIMESTAMP = CURRENT_TIMESTAMP;

CREATE TRIGGER GALADM.TEMPLATES_TR2
   NO CASCADE BEFORE UPDATE
   ON GALADM.TEMPLATES_TBX
   REFERENCING NEW AS A
   FOR EACH ROW
SET UPDATE_TIMESTAMP = CURRENT_TIMESTAMP;