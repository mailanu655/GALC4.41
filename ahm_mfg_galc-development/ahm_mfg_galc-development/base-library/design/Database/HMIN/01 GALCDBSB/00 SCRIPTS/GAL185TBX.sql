DROP TRIGGER GALADM.GAL185_TR2;
DROP TRIGGER GALADM.GAL185_TR1;
DROP TABLE GALADM.GAL185TBX;

CREATE TABLE GALADM.GAL185TBX (
   PRODUCT_ID              CHARACTER (17) NOT NULL DEFAULT '',
   PART_NAME               CHARACTER (32) NOT NULL DEFAULT '',
   PART_SERIAL_NUMBER      VARCHAR (255),
   PASS_TIME               INTEGER,
   INSTALLED_PART_REASON   CHARACTER (128),
   ASSOCIATE_NO            CHARACTER (11),
   ACTUAL_TIMESTAMP        TIMESTAMP,
   INSTALLED_PART_STATUS   INTEGER,
   FIRST_ALARM             INTEGER,
   SECOND_ALARM            INTEGER,
   PART_ID                 CHARACTER (5),
   CREATE_TIMESTAMP        TIMESTAMP,
   UPDATE_TIMESTAMP        TIMESTAMP,
   CONSTRAINT GAL185_PK PRIMARY KEY
     (PRODUCT_ID, PART_NAME)
)
  IN GALTBSDAT6
  INDEX IN GALTBSIDX2;

ALTER TABLE GALADM.GAL185TBX
  DATA CAPTURE CHANGES
  PCTFREE 0
  LOCKSIZE ROW
  APPEND OFF
  NOT VOLATILE;

COMMENT ON TABLE GALADM.GAL185TBX IS 'RMT InstalledPart for LOT CONTROL';

CREATE TRIGGER GALADM.GAL185_TR1
   NO CASCADE BEFORE INSERT
   ON GALADM.GAL185TBX
   REFERENCING NEW AS A
   FOR EACH ROW
SET CREATE_TIMESTAMP = CURRENT_TIMESTAMP;

CREATE TRIGGER GALADM.GAL185_TR2
   NO CASCADE BEFORE UPDATE
   ON GALADM.GAL185TBX
   REFERENCING NEW AS A
   FOR EACH ROW
SET UPDATE_TIMESTAMP = CURRENT_TIMESTAMP;

CREATE INDEX GALADM.GAL185_I1
  ON GALADM.GAL185TBX
    ( PART_NAME ASC, PART_SERIAL_NUMBER ASC, PRODUCT_ID ASC )
  ALLOW REVERSE SCANS
  COMPRESS NO;

CREATE INDEX GALADM.GAL185_I2
  ON GALADM.GAL185TBX
    ( PART_NAME ASC, PRODUCT_ID ASC, PART_SERIAL_NUMBER ASC )
  ALLOW REVERSE SCANS
  COMPRESS NO;