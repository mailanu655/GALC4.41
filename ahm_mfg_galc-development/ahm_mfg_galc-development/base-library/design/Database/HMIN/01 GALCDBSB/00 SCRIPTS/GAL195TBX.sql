DROP TRIGGER GALADM.GAL195_TR1;
DROP TABLE GALADM.GAL195TBX;

CREATE TABLE GALADM.GAL195TBX (
  LINE_ID CHARACTER(16) NOT NULL DEFAULT '',
  SITE_NAME CHARACTER(16) NOT NULL DEFAULT '',
  PLANT_NAME CHARACTER(16) NOT NULL DEFAULT '',
  DIVISION_ID CHARACTER(16),
  DIVISION_NAME CHARACTER(32) NOT NULL DEFAULT '',
  DIVISION_NAME1 CHARACTER(32) NOT NULL DEFAULT '',
  DIVISION_NAME2 CHARACTER(32) NOT NULL DEFAULT '',
  LINE_NAME CHARACTER(32) NOT NULL DEFAULT '',
  LINE_TYPE INTEGER,
  LINE_DESCRIPTION CHARACTER(128),
  ENTRY_PROCESS_POINT_ID CHARACTER(16),
  LINE_SEQUENCE_NUMBER INTEGER,
  STD_INVENTORY INTEGER,
  MAXIMUM_INVENTORY INTEGER,
  MINIMUM_INVENTORY INTEGER,
  CREATE_TIMESTAMP TIMESTAMP,
  UPDATE_TIMESTAMP TIMESTAMP,
  CONSTRAINT GAL195_PK PRIMARY KEY
