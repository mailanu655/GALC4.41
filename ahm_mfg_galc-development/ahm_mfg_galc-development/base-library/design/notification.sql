CREATE TABLE GALADM.NOTIFICATION_TBX (
  NOTIFICATION_CLASS VARCHAR(256) NOT NULL,
  DESCRIPTION VARCHAR(256),
  CLIENT_ONLY SMALLINT DEFAULT 0,
  CREATE_TIMESTAMP TIMESTAMP,
  UPDATE_TIMESTAMP TIMESTAMP,
  PRIMARY KEY(NOTIFICATION_CLASS)
);

CREATE TABLE GALADM.NOTIFICATION_SUBSCRIBER_TBX (
  NOTIFICATION_CLASS VARCHAR(256) NOT NULL,
  CLIENT_NAME CHAR(32),
  CLIENT_IP CHAR(15) NOT NULL,
  CLIENT_PORT INTEGER NOT NULL,
  CREATE_TIMESTAMP TIMESTAMP,
  UPDATE_TIMESTAMP TIMESTAMP,
  PRIMARY KEY(NOTIFICATION_CLASS,CLIENT_IP,CLIENT_PORT),
  FOREIGN KEY(NOTIFICATION_CLASS)
    REFERENCES GALADM.NOTIFICATION_TBX(NOTIFICATION_CLASS)
      ON DELETE NO ACTION
      ON UPDATE NO ACTION
);

CREATE TABLE GALADM.NOTIFICATION_PROVIDER_TBX (
  NOTIFICATION_CLASS VARCHAR(256) NOT NULL,
  HOST_IP CHAR(15) NOT NULL,
  HOST_PORT SMALLINT NOT NULL,
  HOST_NAME CHAR(32),
  DESCRIPTION VARCHAR(256),
  CREATE_TIMESTAMP TIMESTAMP,
  UPDATE_TIMESTAMP TIMESTAMP,
  PRIMARY KEY(NOTIFICATION_CLASS,HOST_IP,HOST_PORT),
  FOREIGN KEY(NOTIFICATION_CLASS)
    REFERENCES GALADM.NOTIFICATION_TBX(NOTIFICATION_CLASS)
      ON DELETE NO ACTION
      ON UPDATE NO ACTION
);