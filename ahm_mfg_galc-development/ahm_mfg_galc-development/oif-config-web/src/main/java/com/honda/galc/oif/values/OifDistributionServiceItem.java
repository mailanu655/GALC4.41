package com.honda.galc.oif.values;

public class OifDistributionServiceItem {

	private String interfaceName;
	private String distributionParam;
	
	public void setInterfaceName(String interfaceName) {
		this.interfaceName = interfaceName;
	}
	public String getInterfaceName() {
		return interfaceName;
	}
	public void setDistributionParam(String distributionParam) {
		this.distributionParam = distributionParam;
	}
	public String getDistributionParam() {
		return distributionParam;
	}
	
}
