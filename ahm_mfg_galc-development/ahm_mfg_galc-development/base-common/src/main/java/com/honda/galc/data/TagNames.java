	public static TagNames getDiecastMarriedPartName(ProductType prodType) {
		switch(prodType){
		case HEAD:
			return HEAD_PART_NAME;
		case BLOCK:
		    return BLOCK_PART_NAME;
		case CRANKSHAFT:
			return CRANKSHAFT_PART_NAME;
		case CONROD:
			return CONROD_PART_NAME;
		default:
			return null;
		}
		
	}
